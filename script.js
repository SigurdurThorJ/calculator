const add = function(add1, add2) {
    const sum = add1 + add2;
    return sum;
};
console.log(add(2, 2));

const subtract = function(sub1, sub2) {
    const sum = sub1 - sub2; 
    return sum;
};
console.log(subtract(2, 2));

const multiply = function(mult1, mult2) {
    let sum = mult1 * mult2;
    return sum; 
};
console.log(multiply(2, 2));

const divide = function(num1, num2) {
    let sum =  num1 / num2;
    return sum;
}
console.log(divide(12, 2));